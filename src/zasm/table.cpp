// Don't rely on `command_list` to be indexed by command.
static constexpr auto sort_script_commands()
{
	std::array<const script_command*, NUMCOMMANDS> sc_by_command{};
	bool seen[NUMCOMMANDS] = {};
	for (auto& sc : command_list)
	{
		if (seen[sc.command])
			throw "Duplicate command found";
		seen[sc.command] = true;
		sc_by_command[sc.command] = &sc;
	}
	return sc_by_command;
}
static constexpr auto sc_by_command = sort_script_commands();

const script_command* get_script_command(int command)
{
	if (command == 0xFFFF)
		return &null_command;
	if (command >= NUMCOMMANDS)
		return nullptr;

	return sc_by_command[command];
}

const script_command* get_script_command(const std::string& name)
{
	for (auto& sc : command_list)
	{
		if (sc.name == name)
			return &sc;
	}

	if (name == "0xFFFF")
		return &null_command;

	return nullptr;
}

std::pair<const script_variable*, int> get_script_variable(int32_t var)
{
	for (auto& sv : variable_list)
	{
		if (sv.maxcount > 0)
		{
			int32_t start = sv.id;
			if (var >= start && var < start + sv.maxcount)
			{
				for(int32_t w = 0; w < sv.maxcount; ++w)
				{
					if (var != start + w) continue;

					return {&sv, w};
				}
			}
		}
		else if (sv.id == var) return {&sv, 0};
	}

	return {nullptr, 0};
}

std::optional<int> get_script_variable(const std::string& var_name)
{
	// Do some quicker checks first.
	if (var_name.size() == 2)
	{
		if (var_name[0] == 'D' || var_name[0] == 'd')
		{
			int c = var_name[1] - '0';
			if (c >= 0 && c < INITIAL_D) return D(c);
		}
	}
	else if ((var_name[0] == 'G' || var_name[0] == 'g') && (var_name[1] == 'D' || var_name[1] == 'd'))
	{
		int c = std::stoi(var_name.substr(2));
		if (c >= 0 && c < 1024) return GD(c);
	}

	for (int q = 0; variable_list[q].id != -1; ++q)
	{
		if (variable_list[q].maxcount == 0 && stricmp(var_name.c_str(), variable_list[q].name) == 0)
			return variable_list[q].id;
	}

	return std::nullopt;
}

