static void test_bitstring_json()
{
	// randomly generated 100 bytes to test with
	std::vector<byte> input_data = { 0x80, 0xF9, 0x79, 0x07, 0x86, 0x15, 0x07, 0xFA, 0xF8, 0x29, 0x65, 0xF9, 0xAB, 0x24, 0x76, 0xF2, 0x53, 0x84, 0xCF, 0xFE, 0xB6, 0x60, 0x6F, 0xCF, 0xDC, 0x29, 0x7E, 0xD0, 0x19, 0xAE, 0x0C, 0x8B, 0xDF, 0x71, 0x09, 0xBD, 0x5C, 0xAF, 0xE2, 0x8F, 0xC8, 0x7D, 0x74, 0x46, 0xDB, 0x7F, 0x61, 0x89, 0xE5, 0x03, 0x1E, 0xC5, 0x85, 0x63, 0xF1, 0x05, 0xF7, 0xD9, 0x1C, 0xD6, 0x99, 0xB0, 0xCD, 0x2B, 0x22, 0x1E, 0x26, 0x6B, 0x48, 0xD7, 0x0B, 0xB2, 0x8D, 0xAE, 0x7D, 0xAE, 0xC3, 0x68, 0x9B, 0x9F, 0x95, 0x63, 0x6C, 0xAA, 0x79, 0x4E, 0xAD, 0x2D, 0x7A, 0xA8, 0xE7, 0xA7, 0xE4, 0xA1, 0x8D, 0x76, 0x27, 0xF9, 0x89, 0x1E };
	
	bitstring input;
	input.inner() = input_data;
	
	json j = input;
	
	bitstring output;
	j.get_to(output);
	
	assertEqual(input.inner().inner(), output.inner().inner());
	assertEqual(input.inner().inner(), input_data);
	assertEqual(output.inner().inner(), input_data);
}

TestResults test_containers(bool verbose)
{
	TestResults tr{};
	struct { const char* name; void (*fn)(); } tests[] = {
		{ "bitstring_json", test_bitstring_json }
	};
	
	for (auto& test : tests)
	{
		++tr.total;
		try
		{
			test.fn();
			if (verbose)
				fmt::println("  [PASS] {}", test.name);
		}
		catch (const std::exception& e)
		{
			++tr.failed;
			fmt::println("  [FAIL] {}\n{}", test.name, e.what());
		}
	}

	return tr;
}

